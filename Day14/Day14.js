function getRobots() {
    const inputData = getInputData();
    const lines = inputData.trim().split("\n");
    const pattern = /p=(-?\d+),(-?\d+)\s+v=(-?\d+),(-?\d+)/;
    const robots = [];

    for (const line of lines) {
        const [_, px, py, vx, vy] = line.match(pattern).map(Number);

        const robot = {
            px: px,
            py: py,
            vx: vx,
            vy: vy
        }
        robots.push(robot)
    }
    return robots;
}

function tick(robot) {
    let nextPosition = move(robot);

    robot.px = nextPosition.px;
    robot.py = nextPosition.py;
}

function move(robot) {
    let px = (robot.px + robot.vx) % tiles.x;
    if (px < 0) {
        //px = tiles.x - Math.abs(px);
        px = tiles.x + px;
    }
    let py = (robot.py + robot.vy) % tiles.y;
    if (py < 0) {
        py = tiles.y + py;
    }
    const move = {
        px: px,
        py: py,
    }

    return move;
}


function getSafetyFactor(robots) {
    let q1 = 0, q2 = 0, q3 = 0, q4 = 0;
    const xMid = (tiles.x - 1 ) / 2;
    const yMid = (tiles.y - 1 ) / 2;

    for (const robot of robots) {
        if (robot.px < xMid && robot.py < yMid) {q1++;}
        if (robot.px > xMid && robot.py < yMid) {q2++;}
        if (robot.px < xMid && robot.py > yMid) {q3++;}
        if (robot.px > xMid && robot.py > yMid) {q4++;}
    }
    return q1 * q2 * q3 * q4;
}

